class Array33 {
  public static void main(final String[] args) {
    final int[][] a = {{1,2,3},{4,5,6},{7,8,9}};
    System.out.println(a[0][0] + " " + a[0][1] + " " + a[0][2]);
    System.out.println(a[1][0] + " " + a[1][1] + " " + a[1][2]);
    System.out.println(a[2][0] + " " + a[2][1] + " " + a[2][2]);
  }
}
