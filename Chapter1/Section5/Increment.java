class Increment {
  public static void main(final String[] args) {
    int i = 0;
    System.out.println(++i);
    System.out.println(i);
    System.out.println(i++);
    System.out.println(i);
    System.out.println(--i);
    System.out.println(i);
    System.out.println(i--);
    System.out.println(i);
  }
}